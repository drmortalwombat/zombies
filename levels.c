void level_start(const Level * l)
{
	level = l;
	level_cmd = 0;
	level_delay = 0;
	level_conveyor = 0;

	level_size = 0;
	while (level->cmds[level_size] != LVC_END)
		level_size++;

	char row = 0;
	for(char i=0; i<32; i++)
	{		
		do
		{
			row++;
			if (row == 5)
				row = 0;
		}
		while (!(l->rows & (1 << row)));

		level_rows[i] = row;
	}

	if (l->flags & LF_NIGHT)
	{
		back_color = VCOL_PURPLE << 4;
		back_tile = PT_NONE_NIGHT;
	}
	else
	{
		back_color = VCOL_GREEN << 4;
		back_tile = PT_NONE_DAY;
	}

	plant_draw_borders();

	if (l->flags & LF_CONVEYOR)
		menu_init(10);
	else
		menu_init(l->slots + 1);

	mower_init();

	plant_grid_clear(l->rows);

	for(char i=0; i<l->graves; i++)
	{
		char x, y;
		do {
			y = rand() % 5;
			x = 4 + rand() % 5;
		} while (plant_grid[y][x].type > PT_GROUND);

		plant_place(x, y, PT_TOMBSTONE);
	}

	plant_grid_draw();

	menu_progress(0, level_size);

	unsigned	seeds = l->seeds;
	if (l->flags & LF_CONVEYOR)
	{
		for(char i=0; i<9; i++)
			menu_add_item(PT_CONVEYOR, 0, 0, false, false);

		if (l->flags & LF_SHOVEL)
			menu_add_item(PT_SHOVEL, 0, 0, true, false);
	}
	else
	{
		menu_add_item(PT_SUN, l->sun, 0, true, false);
		if (l->flags & LF_FIXSEED)
		{
			for(char i=0; i<15; i++)
			{
				if (seeds & (1u << i))
					menu_add_item(seed_info[i].plant, seed_info[i].cost, seed_info[i].warm, seed_info[i].ready, false);
			}
		}
		else
		{
			seeds_edit_menu(seeds, l->slots);
			plant_grid_draw();
		}

		if (l->flags & LF_SHOVEL)
			menu_add_item(PT_SHOVEL, 0, 0, true, false);
	}


	music_init(l->tune);

	music_active = true;
}

void level_iterate(void)
{
	if (level->flags & LF_CONVEYOR)
	{
		if (level_conveyor > 0)
			level_conveyor--;
		else
		{
			char x = 0;
			while (x < menu_size && menu[x].type != PT_CONVEYOR)
				x++;
			if (x < menu_size)
			{
				unsigned	s = 0;
				do {
					s = rand() & 15;
				} while (!(level->seeds & (1 << s)));

				menu_add_item_at(x, seed_info[s].plant, 0, 32, false, true);
			}

			level_conveyor = 20;
		}
	}

	if (!level_delay)
	{
		switch(level->cmds[level_cmd] & 0xf0)
		{
			case LVC_DELAY_10F:
				level_delay = 1;
				break;
			case LVC_DELAY_20F:
				level_delay = 2;
				break;
			case LVC_DELAY_30F:
				level_delay = 3;
				break;
			case LVC_DELAY_1S:
				level_delay = 5;
				break;
			case LVC_DELAY_2S:
				level_delay = 4;
				break;
			case LVC_DELAY_5S:
				level_delay = 25;
				break;
			case LVC_DELAY_10S:
				level_delay = 50;
				break;
			case LVC_DELAY_15S:
				level_delay = 75;
				break;
			case LVC_DELAY_20S:
				level_delay = 100;
				break;
			case LVC_DELAY_25S:
				level_delay = 125;
				break;
			case LVC_DELAY_30S:
				level_delay = 150;
				break;
		}
	}
	else
	{
		level_delay--;
		if (!level_delay)
		{
			char 	i = rand() & 15;
			char	row = level_rows[i];
			while (i < 31)
			{
				level_rows[i] = level_rows[i + 1];
				i++;
			}
			level_rows[31] = row;

			bool	done = true;

			switch (level->cmds[level_cmd] & 0x0f)
			{
				case LVC_ZOMBIE:
					done = zombies_add(172, row, ZOMBIE_BASE, 0);
					break;

				case LVC_ZOMBIE_CONE:
					done = zombies_add(172, row, ZOMBIE_CONE, 0);
					break;			

				case LVC_ZOMBIE_POLE:
					done = zombies_add(172, row, ZOMBIE_POLE, 0);
					break;			

				case LVC_ZOMBIE_BUCKET:
					done = zombies_add(172, row, ZOMBIE_BUCKET, 0);
					break;			

				case LVC_ZOMBIE_PAPER:
					done = zombies_add(172, row, ZOMBIE_PAPER, 0);
					break;			

				case LVC_ZOMBIE_SCREENDOOR:
					done = zombies_add(172, row, ZOMBIE_SCREENDOOR, 0);
					break;			

				case LVC_ZOMBIE_FOOTBALL:
					done = zombies_add(172, row, ZOMBIE_FOOTBALL, 0);
					break;						

				case LVC_ZOMBIE_DANCER:
					done = zombies_add(172, row, ZOMBIE_DANCER, 0);
					break;						

				case LVC_ZOMBIE_GRAVES:
					zombies_grave(ZOMBIE_RESURRECT);
					break;			
			}

			if (done)
			{
				level_cmd++;
				menu_progress(level_cmd, level_size);
			}
			else
				level_delay = 5;
		}
	}
}

bool level_complete(void)
{
	return level->cmds[level_cmd] == LVC_END;
}

